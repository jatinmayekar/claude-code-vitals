"""Init command for claude_code_vitals.

Sets up:
1. ~/.claude-code-vitals/ data directory
2. ~/.claude-code-vitals/config.toml with defaults
3. Claude Code statusLine configuration in ~/.claude/settings.json
"""

import json
import sys
import shutil
from pathlib import Path

from .config import Config, CLAUDE_SETTINGS_PATH, write_default_config


STATUSLINE_COMMAND = "python3 -m claude_code_vitals run"


def init(config: Config) -> None:
    """Initialize ccvitals — one-time setup."""
    print("⚡ ccvitals — initializing...\n")

    # 1. Create data directory
    config.ensure_data_dir()
    print(f"  ✓ Data directory: {config.data_dir}")

    # 2. Write default config
    if not config.config_path.exists():
        write_default_config(config)
        print(f"  ✓ Config file: {config.config_path}")
    else:
        print(f"  · Config exists: {config.config_path}")

    # 3. Configure Claude Code statusLine
    success = _configure_statusline()
    if success:
        print(f"  ✓ Claude Code statusLine configured")
    else:
        print(f"  ⚠ Could not auto-configure Claude Code statusLine")
        print(f"    Add this to {CLAUDE_SETTINGS_PATH}:")
        print(f'    "statusLine": {{"type": "command", "command": "{STATUSLINE_COMMAND}"}}')

    # Done
    print(f"\n🎉 ccvitals is ready!")
    print(f"   Restart Claude Code to activate the status bar.")
    print(f"   Data will be logged to: {config.history_path}")
    print(f"\n   Run 'ccvitals status' to check current state.")


def _configure_statusline() -> bool:
    """Add claude_code_vitals to Claude Code's settings.json statusLine config.
    
    If statusLine is already configured, we create a wrapper script
    that runs both the existing command and claude_code_vitals.
    """
    settings_path = CLAUDE_SETTINGS_PATH

    # Read existing settings
    settings = {}
    if settings_path.exists():
        try:
            settings = json.loads(settings_path.read_text())
        except (json.JSONDecodeError, PermissionError):
            return False

    existing_sl = settings.get("statusLine")

    if existing_sl is None:
        # No existing statusLine — simple case
        settings["statusLine"] = {
            "type": "command",
            "command": STATUSLINE_COMMAND,
        }
    elif isinstance(existing_sl, dict):
        existing_cmd = existing_sl.get("command", "")
        if "ccvitals" in existing_cmd or "claude_code_vitals" in existing_cmd:
            # Already configured
            print(f"  · statusLine already has ccvitals")
            return True

        if "limitwatch" in existing_cmd:
            # Legacy pre-rename installation — upgrade in place (not wrap).
            # The old `limitwatch` binary is the same tool as us, just under
            # the old name. Wrapping it would either invoke a stale binary or,
            # if the binary has been uninstalled, break the statusLine.
            settings["statusLine"] = {
                "type": "command",
                "command": STATUSLINE_COMMAND,
            }
            print(f"  ✓ Upgraded legacy limitwatch statusLine to ccvitals")
        else:
            # Existing third-party statusLine — create a wrapper that runs both.
            # The user's existing statusline gets priority for display;
            # claude_code_vitals runs silently for logging only in this mode.
            wrapper_path = _create_wrapper_script(existing_cmd)
            settings["statusLine"] = {
                "type": "command",
                "command": f"bash {wrapper_path}",
            }
            print(f"  ℹ Wrapped existing statusLine command with claude_code_vitals")
    else:
        # Unknown format — just set it
        settings["statusLine"] = {
            "type": "command",
            "command": STATUSLINE_COMMAND,
        }

    # Write back
    try:
        settings_path.parent.mkdir(parents=True, exist_ok=True)
        settings_path.write_text(json.dumps(settings, indent=2) + "\n")
        return True
    except (PermissionError, OSError):
        return False


def _create_wrapper_script(existing_cmd: str) -> Path:
    """Create a shell script that runs both the existing statusline and claude_code_vitals.
    
    The stdin JSON goes to both. claude_code_vitals logs silently; the existing
    command's output is what gets displayed.
    """
    wrapper_path = Path.home() / ".claude-code-vitals" / "statusline-wrapper.sh"
    script = f"""#!/bin/bash
# claude_code_vitals wrapper — runs both your existing statusline and claude_code_vitals
# Generated by: claude_code_vitals init

INPUT=$(cat)

# Run claude_code_vitals in background for logging (output to /dev/null)
echo "$INPUT" | python3 -m claude_code_vitals run --log-only &>/dev/null &

# Run existing statusline command and display its output
echo "$INPUT" | {existing_cmd}
"""
    wrapper_path.write_text(script)
    wrapper_path.chmod(0o755)
    return wrapper_path


def uninstall(config: Config) -> None:
    """Remove claude_code_vitals configuration."""
    print("🧹 claude_code_vitals — uninstalling...\n")

    # Remove from Claude Code settings
    if CLAUDE_SETTINGS_PATH.exists():
        try:
            settings = json.loads(CLAUDE_SETTINGS_PATH.read_text())
            sl = settings.get("statusLine", {})
            sl_cmd = sl.get("command", "") if isinstance(sl, dict) else ""
            if isinstance(sl, dict) and ("ccvitals" in sl_cmd or "claude_code_vitals" in sl_cmd or "limitwatch" in sl_cmd):
                del settings["statusLine"]
                CLAUDE_SETTINGS_PATH.write_text(json.dumps(settings, indent=2) + "\n")
                print("  ✓ Removed from Claude Code statusLine")
        except Exception:
            pass

    # Remove wrapper script
    wrapper = config.data_dir / "statusline-wrapper.sh"
    if wrapper.exists():
        wrapper.unlink()
        print("  ✓ Removed wrapper script")

    # Ask about data
    print(f"\n  Data directory: {config.data_dir}")
    print("  To delete all data: rm -rf ~/.claude-code-vitals")
    print("  To keep data but stop monitoring: done (statusLine removed)")
